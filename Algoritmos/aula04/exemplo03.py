
fruta = input("Fruta:").lower()
if(fruta == "banana"):
    print("Preço por quilo da ", fruta, ": R$5,23")
elif(fruta == "maça"):
    print("Preço por quilo da ", fruta, ": 12,10")
elif(fruta == "cereja"):
    print("Preço por quilo da ", fruta, ": 58,00")
else:
    print("Não achamos esta fruta: ", fruta)

